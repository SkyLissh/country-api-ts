class Country {
	name: string;
	population: number;
	region: string;
	capital: string;
	flag: string;

	constructor(
		name: string,
		population: number,
		region: string,
		capital: string,
		flag: string
	) {
		this.name = name;
		this.population = population;
		this.region = region;
		this.capital = capital;
		this.flag = flag;
	}
}
